
public class Course {
	
	private String name;
	private int points;
	
	public Course(String aName, int somePoints) {
		
		name = aName;
		points = somePoints;
	}
	
	
		//getters
	
	public String getName() {
		
		return name;
	}
	
	public int getPoints() {
		
		return points;
	}
	
	
		//setters
	
	public void setName(String name) {
		
		this.name = name;
	}
	
	public void setpoints(int points) {
		
		this.points = points;
	}

}






